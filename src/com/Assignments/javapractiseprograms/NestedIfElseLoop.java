package com.Assignments.javapractiseprograms;

public class NestedIfElseLoop {
    public static void main(String[] args) {
        int i=10;
        if(i==20){
            System.out.println("no is 20");
        } else if (i==15) {
            System.out.println("no is 15");
        } else if (i==10) {
            System.out.println("no is 10");
        }else{
            System.out.println("i is not present");
        }
    }
}
